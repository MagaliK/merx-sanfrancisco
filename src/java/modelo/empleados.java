public class empleados extends cajas {

    int id_empleado;
    String nombre_emp;
    String apellido_emp;
    String ndocumento_emp;
    String fnac_emp;
    String direccion_emp;
    String telefono_emp;
    String celular_emp;
    String observacion_emp;
    String close_emp;
    String pagina_inicio;

    public int getId_empleado() {
        return id_empleado;
    }

    public void setId_empleado(int id_empleado) {
        this.id_empleado = id_empleado;
    }

    public String getNombre_emp() {
        return nombre_emp;
    }

    public void setNombre_emp(String nombre_emp) {
        this.nombre_emp = nombre_emp;
    }

    public String getApellido_emp() {
        return apellido_emp;
    }

    public void setApellido_emp(String apellido_emp) {
        this.apellido_emp = apellido_emp;
    }

    public String getNdocumento_emp() {
        return ndocumento_emp;
    }

    public void setNdocumento_emp(String ndocumento_emp) {
        this.ndocumento_emp = ndocumento_emp;
    }

    public String getFnac_emp() {
        return fnac_emp;
    }

    public void setFnac_emp(String fnac_emp) {
        this.fnac_emp = fnac_emp;
    }

    public String getDireccion_emp() {
        return direccion_emp;
    }

    public void setDireccion_emp(String direccion_emp) {
        this.direccion_emp = direccion_emp;
    }

    public String getTelefono_emp() {
        return telefono_emp;
    }

    public void setTelefono_emp(String telefono_emp) {
        this.telefono_emp = telefono_emp;
    }

    public String getCelular_emp() {
        return celular_emp;
    }

    public void setCelular_emp(String celular_emp) {
        this.celular_emp = celular_emp;
    }

    public String getObservacion_emp() {
        return observacion_emp;
    }

    public void setObservacion_emp(String observacion_emp) {
        this.observacion_emp = observacion_emp;
    }

    public String getClose_emp() {
        return close_emp;
    }

    public void setClose_emp(String close_emp) {
        this.close_emp = close_emp;
    }

    public String getPagina_inicio() {
        return pagina_inicio;
    }

    public void setPagina_inicio(String pagina_inicio) {
        this.pagina_inicio = pagina_inicio;
    }

}
